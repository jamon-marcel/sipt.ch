      <p>Das Schweizer Institut für Psychotraumatologie (SIPT) mit Sitz in Winterthur wurde 2006 gegründet. Seit seiner Gründung arbeitet das SIPT in Kooperation mit dem Deutschen Instituts für Psychotraumatologie (DIPT) und dem Zentrum für interdisziplinäre Therapien in Konstanz, seit 2014 zusätzlich mit dem Zentrum für Trauma- und Konfliktmangement (ZTK) in Köln und dem Institut für Trauma-Bearbeitung und Weiterbildung in Frankfurt.</p>
