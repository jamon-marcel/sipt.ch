    <h2>
      <a href="javascript:;" class="btn-collapsible js-clpsbl-btn">Fortbildungskonzept</a>
    </h2>
    <div class="collapsible__content js-clpsbl-body" style="display: none">
      <p>Das SIPT bietet Fortbildungen für PsychotherapeutInnen, BeraterInnen, PädagogInnen und andere Berufsgruppen an, die in ihrem Arbeitsbereich mit traumatisierten Menschen konfrontiert sind. Fachpersonen, die mit Traumaopfern arbeiten, sollten neben ihrer berufsspezifischen Ausbildung weitergebildet sein im Erkennen von psychotraumatischen Syndromen und deren Komorbiditäten. Der Umgang mit Traumafolgeerscheinungen einschliesslich psychoedukativer Massnahmen, der Anleitung zur Krisenintervention und der Planung adäquater Behandlungsstrategien bedarf einer soliden Zusatzausbildung. Im Fortbildungsangebot des SIPT stehen psychodynamische Konzepte im Vordergrund.</p>
      <p>Unser Fortbildungskonzept zeichnet sich durch zwei Aspekte aus:</p>
      <ul>
        <li>intensive Schulung in einem traumatherapeutischen Verfahren (Mehrdimensionale Psychodynamische Traumatherapie (MPTT)) oder in traumazentrierter Beratung</li>
        <li>praxisnaher Unterricht in Seminarform und Übungen in Kleingruppen durch anerkannte Fachpersonen aus dem In- und Ausland</li>
      </ul>
