<p>Liebe Kolleginnen und Kollegen</p>
<p>Gerne möchten wir Sie auf unser aktuelles Kursangebot „Traumatisierte Flüchtlinge im Schulalltag“ aufmerksam machen.<br>Sie lernen in diesem Kurs, wie sich eine Fluchterfahrung auf Kinder auswirken kann. Zusammen mit unserer erfahrenen Dozentin Ruth Monstein gehen Sie Fragen nach, wie:</p>
<ul>
<li>Auf welche Weise zeigen sich Traumatisierungen?</li>
<li>Welche Chancen bietet ein Schulalltag, um Kinder zu stabilisieren und was fördert ihre Resilienz?</li>
<li>Welche sensiblen Momente gilt es im Auge zu behalten, damit keine Retraumatisierungen erfolgen?</li>
</ul>
<p>Ruth Monstein, Fachberaterin Psychotraumatologie und selbst Primarlehrerin, vermittelt Ihnen praxisnah konkrete Übungen zur Stabilisierung von körperlichem, gedanklichem und emotionalen Stress. Persönliche Selbstfürsorge für Sie wird ebenfalls Inhalt des Seminars sein.</p>
